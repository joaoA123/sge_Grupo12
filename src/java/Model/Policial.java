package Model;

/**
 *
 * @author Luis Silva
 */
public class Policial {
     int id_pessoa;
     String user;
     String senha;

    public Policial(int id_pessoa, String user, String senha) {
        this.id_pessoa = id_pessoa;
        this.user = user;
        this.senha = senha;
    }

    public int getId_pessoa() {
        return id_pessoa;
    }

    public void setId_pessoa(int id_pessoa) {
        this.id_pessoa = id_pessoa;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getSenha() {
        return senha;
    }

    public void setSenha(String senha) {
        this.senha = senha;
    }
     
}

